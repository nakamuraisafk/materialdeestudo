# Ler um número inteiro e exibir seu dobro.

# Ler um número inteiro
numero = int(input("Digite um número inteiro: "))  # O input pega o valor que você digitar e converte em um número inteiro

# Calcular o dobro do número
dobro = numero * 2  # Aqui, multiplicamos o número por 2

# Exibir o dobro
print("O dobro do número é:", dobro)  # O print exibe o resultado na tela
