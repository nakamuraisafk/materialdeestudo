# Ler dois números reais
numero1 = float(input("Digite o primeiro número real: "))  # Pede o primeiro número e converte para real
numero2 = float(input("Digite o segundo número real: "))  # Pede o segundo número e converte para real

# Multiplicar os dois números
multiplicacao = numero1 * numero2  # Multiplica os dois números reais

# Exibir o resultado
print("A multiplicação dos dois números é:", multiplicacao)  # Exibe o resultado


# float= Converte o valor digitado para número real (com casas decimais)